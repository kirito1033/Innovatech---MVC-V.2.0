        <h2 class="text-center mb-4">Aviso de Privacidad</h2>
        <p class="text-center"><strong>Última actualización: 5 de junio de 2025</strong></p>

        <p>En Innovatech, nos comprometemos a proteger tu privacidad y a garantizar la seguridad de tu información personal. Este Aviso de Privacidad describe cómo recopilamos, utilizamos, compartimos y protegemos tu información personal cuando visitas nuestro sitio web <a href="https://www.inovatech.com">www.inovatech.com</a> (en adelante, el "Sitio") o interactúas con nosotros a través de otros canales, como correo electrónico o atención al cliente. Al utilizar nuestro Sitio, aceptas las prácticas descritas en este Aviso de Privacidad, que cumple con la Ley 1581 de 2012 de Colombia y, cuando aplica, el Reglamento General de Protección de Datos (GDPR) de la Unión Europea.</p>

        <h3>1. Información que Recopilamos</h3>
        <p>Recopilamos información personal que nos proporcionas directamente, así como datos generados automáticamente durante tu interacción con el Sitio. La información que recopilamos incluye:</p>
        <ul>
            <li><strong>Información de identificación personal:</strong> Nombre completo, dirección de correo electrónico, número de teléfono, dirección de envío y facturación, y detalles de pago (como números de tarjeta de crédito o información bancaria) cuando creas una cuenta, realizas una compra o te comunicas con nosotros.</li>
            <li><strong>Información de transacciones:</strong> Detalles sobre los productos que compras, fechas de las transacciones, montos pagados, métodos de pago y cualquier información relacionada con reembolsos o devoluciones.</li>
            <li><strong>Información de navegación:</strong> Dirección IP, tipo de navegador, sistema operativo, páginas visitadas en el Sitio, tiempo de navegación, y otros datos recopilados a través de cookies y tecnologías similares.</li>
            <li><strong>Comunicaciones con nosotros:</strong> Información que proporcionas al contactarnos por correo electrónico, formularios en el Sitio, chat en línea o nuestro servicio de atención al cliente, incluyendo consultas, quejas o solicitudes.</li>
            <li><strong>Información opcional:</strong> Datos que eliges proporcionar, como preferencias de productos, respuestas a encuestas o participación en promociones o programas de lealtad.</li>
        </ul>
        <p>No recopilamos intencionalmente información personal de menores de 18 años sin el consentimiento de un padre o tutor legal. Si descubrimos que hemos recopilado dicha información, la eliminaremos de inmediato.</p>

        <h3>2. Uso de la Información</h3>
        <p>Utilizamos la información que recopilamos para los siguientes propósitos:</p>
        <ul>
            <li><strong>Procesamiento de pedidos:</strong> Para gestionar tus compras, procesar pagos, coordinar envíos y manejar devoluciones o reembolsos.</li>
            <li><strong>Soporte al cliente:</strong> Para responder a tus consultas, resolver problemas y proporcionarte asistencia personalizada.</li>
            <li><strong>Mejora del Sitio:</strong> Para analizar el uso del Sitio, identificar áreas de mejora y optimizar la experiencia del usuario, como personalizar contenido y recomendaciones de productos.</li>
            <li><strong>Comunicaciones:</strong> Para enviarte notificaciones relacionadas con tus pedidos (por ejemplo, confirmaciones, actualizaciones de envío) y, si has dado tu consentimiento, comunicaciones promocionales, boletines informativos o encuestas de satisfacción.</li>
            <li><strong>Cumplimiento legal:</strong> Para cumplir con nuestras obligaciones legales, como la emisión de facturas, auditorías fiscales o respuesta a solicitudes de autoridades competentes.</li>
            <li><strong>Prevención de fraudes:</strong> Para detectar y prevenir actividades fraudulentas, como intentos de pagos no autorizados o accesos indebidos a cuentas de usuarios.</li>
            <li><strong>Análisis y estadísticas:</strong> Para realizar estudios internos sobre tendencias de compra, comportamiento de los usuarios y desempeño del Sitio, siempre de manera agregada y anónima cuando sea posible.</li>
        </ul>
        <p>Nos aseguramos de procesar tu información únicamente para los fines descritos en este Aviso y de acuerdo con las bases legales establecidas en la legislación aplicable, como tu consentimiento, la ejecución de un contrato o el cumplimiento de obligaciones legales.</p>

        <h3>3. Compartición de Información</h3>
        <p>No compartimos tu información personal con terceros, salvo en los siguientes casos:</p>
        <ul>
            <li><strong>Proveedores de servicios:</strong> Compartimos información con socios que nos ayudan a operar el Sitio y procesar tus pedidos, como empresas de logística para envíos, procesadores de pagos para transacciones seguras y proveedores de servicios de alojamiento o análisis de datos. Estos terceros están obligados contractualmente a proteger tu información y a usarla solo para los fines acordados.</li>
            <li><strong>Requisitos legales:</strong> Podemos divulgar tu información si así lo exige la ley, un proceso legal (como una orden judicial) o para proteger nuestros derechos, propiedad o seguridad, o los de nuestros usuarios o terceros.</li>
            <li><strong>Transferencias comerciales:</strong> En caso de fusión, adquisición o venta de activos, tu información personal puede transferirse a la entidad sucesora, pero solo bajo estrictas medidas de confidencialidad y con notificación previa cuando sea posible.</li>
        </ul>
        <p>No vendemos ni alquilamos tu información personal a terceros para fines de marketing sin tu consentimiento explícito.</p>

        <h3>4. Seguridad de la Información</h3>
        <p>Implementamos medidas de seguridad físicas, electrónicas y administrativas para proteger tu información personal contra accesos no autorizados, pérdida, alteración o divulgación. Estas medidas incluyen:</p>
        <ul>
            <li>Encriptación de datos sensibles, como información de pago, utilizando protocolos SSL/TLS.</li>
            <li>Controles de acceso restringido para nuestros empleados y socios.</li>
            <li>Monitoreo continuo de nuestros sistemas para detectar y prevenir amenazas de seguridad.</li>
            <li>Capacitación regular para nuestro personal sobre prácticas de protección de datos.</li>
        </ul>
        <p>Sin embargo, ninguna transmisión de datos por internet o sistema de almacenamiento es completamente segura. Si detectamos una violación de seguridad que pueda comprometer tu información, te notificaremos lo antes posible y, si corresponde, informaremos a las autoridades de protección de datos pertinentes.</p>

        <h3>5. Cookies y Tecnologías Similares</h3>
        <p>Utilizamos cookies y tecnologías similares, como píxeles de seguimiento y balizas web, para los siguientes fines:</p>
        <ul>
            <li><strong>Cookies esenciales:</strong> Permiten funciones básicas del Sitio, como la navegación, el acceso a áreas seguras y la gestión del carrito de compras.</li>
            <li><strong>Cookies de rendimiento:</strong> Recopilan datos anónimos sobre cómo los usuarios interactúan con el Sitio para mejorar su funcionalidad y rendimiento.</li>
            <li><strong>Cookies de personalización:</strong> Permiten recordar tus preferencias, como idioma o región, para ofrecer una experiencia personalizada.</li>
            <li><strong>Cookies de publicidad:</strong> Utilizadas para mostrarte anuncios relevantes según tus intereses, tanto en nuestro Sitio como en sitios de terceros.</li>
        </ul>
        <p>Puedes configurar tu navegador para rechazar todas o algunas cookies, o para que te avise cuando se envíen cookies. Sin embargo, deshabilitar cookies esenciales puede limitar el funcionamiento del Sitio. Para más información sobre las cookies que utilizamos, consulta nuestra <a href="politica-cookies.html">Política de Cookies</a>.</p>

        <h3>6. Tus Derechos</h3>
        <p>De acuerdo con la Ley 1581 de 2012 de Colombia y, cuando aplique, el GDPR, tienes los siguientes derechos con respecto a tu información personal:</p>
        <ul>
            <li><strong>Acceso:</strong> Solicitar una copia de la información personal que tenemos sobre ti.</li>
            <li><strong>Rectificación:</strong> Corregir datos inexactos o incompletos.</li>
            <li><strong>Eliminación:</strong> Solicitar la eliminación de tu información personal, salvo en casos donde estemos obligados a conservarla por requisitos legales.</li>
            <li><strong>Oposición:</strong> Oponerte al procesamiento de tu información para fines específicos, como marketing directo.</li>
            <li><strong>Restricción:</strong> Solicitar que restrinjamos el procesamiento de tu información en ciertas circunstancias.</li>
            <li><strong>Portabilidad:</strong> Recibir tu información en un formato estructurado y transferirla a otro proveedor, cuando sea técnicamente posible.</li>
            <li><strong>Retiro de consentimiento:</strong> Retirar tu consentimiento para el procesamiento de datos en cualquier momento, sin afectar la legalidad del procesamiento previo.</li>
        </ul>
        <p>Para ejercer estos derechos, contáctanos en <a href="mailto:privacidad@inovatech.com">privacidad@inovatech.com</a>. Responderemos a tu solicitud dentro de los plazos establecidos por la ley (generalmente 15 días hábiles en Colombia o 30 días bajo GDPR). Es posible que te pidamos verificar tu identidad para procesar tu solicitud.</p>
        <p>Si consideras que no hemos manejado adecuadamente tu información personal, tienes derecho a presentar una queja ante la Superintendencia de Industria y Comercio en Colombia o la autoridad de protección de datos de tu jurisdicción.</p>

        <h3>7. Transferencias Internacionales de Datos</h3>
        <p>Si accedes al Sitio desde fuera de Colombia, tu información personal puede transferirse y procesarse en servidores ubicados en otros países, como Estados Unidos, donde operan algunos de nuestros proveedores de servicios. Nos aseguramos de que dichas transferencias cumplan con las leyes de protección de datos aplicables, utilizando mecanismos como cláusulas contractuales estándar o acuerdos de transferencia de datos.</p>
        <p>Al utilizar el Sitio, aceptas la transferencia internacional de tu información personal para los fines descritos en este Aviso.</p>

        <h3>8. Retención de Datos</h3>
        <p>Conservamos tu información personal solo durante el tiempo necesario para cumplir con los fines descritos en este Aviso, incluyendo:</p>
        <ul>
            <li>El tiempo requerido para procesar tus pedidos y cumplir con garantías o devoluciones.</li>
            <li>El período necesario para cumplir con obligaciones legales, como requisitos fiscales o contables (generalmente 5 años en Colombia).</li>
            <li>El tiempo necesario para resolver disputas o hacer valer nuestros derechos legales.</li>
        </ul>
        <p>Una vez que tu información ya no sea necesaria, la eliminaremos o anonimizaremos de manera segura, salvo que la ley exija lo contrario.</p>

        <h3>9. Cambios en el Aviso de Privacidad</h3>
